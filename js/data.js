class Favoritos {
    constructor(localStorageKey) {
        this.localStorageKey = localStorageKey;

        const favoritosFromStorage = localStorage.getItem(this.localStorageKey);
        if (favoritosFromStorage) {
            this.favoritos = new Set(JSON.parse(favoritosFromStorage));
        } else {
            this.favoritos = new Set();
        }
    }
    saveToLocalStorage() {
        localStorage.setItem(this.localStorageKey, JSON.stringify(Array.from(this.favoritos)));
    }
    toggleFavorito(id) {
        if (this.favoritos.has(id)) {
            this.favoritos.delete(id);
            console.log(`ID ${id} eliminada de favoritos.`);
        } else {
            this.favoritos.add(id);
            console.log(`ID ${id} añadida a favoritos.`);
        }
        this.saveToLocalStorage(); 
    }
    esFavorito(id) {
        return this.favoritos.has(id);
    }
    obtenerFavoritos() {
        return Array.from(this.favoritos);
    }
}